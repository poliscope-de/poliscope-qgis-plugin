    def btnHandlerRefresh_focusregion(self, force=False):
        if not self.api:
            return

        QApplication.setOverrideCursor(Qt.WaitCursor)
        try:
            # Load focusregions if not yet cached
            if self._focusregions is None:
                focusregions, _ = self.api.get_focusregions(limit=500, detail="standard")
                if not focusregions:
                    return
                self._populate_focusregion_radio_buttons(focusregions)
                self._focusregions = self._deduped_focusregions

            # Get selected focusregion
            fr = self._get_selected_focusregion()
            if not fr:
                return

            nur_neuigkeiten = self.cbNurNeuigkeiten_focusregion.isChecked()
            last_visit = next(
                (m.get('lastVisit') for m in (fr.team or []) if m.get('lastVisit')), None)
            cache_key = (fr.id, nur_neuigkeiten)

            if force:
                self._focusregion_cache.pop((fr.id, True), None)
                self._focusregion_cache.pop((fr.id, False), None)

            if cache_key in self._focusregion_cache:
                result_groups = self._focusregion_cache[cache_key]
            else:
                new_since = last_visit if (nur_neuigkeiten and last_visit) else None
                result_groups, _, _ = self.api.get_focusregion_results(fr.id, new_since=new_since)
                if result_groups is not None:
                    self._focusregion_cache[cache_key] = result_groups
        finally:
            QApplication.restoreOverrideCursor()

        if result_groups is None:
            return

        # Client-side filter + sort
        filtered = self._filter_focusregion_results(result_groups)
        self.results_focusregion = filtered
        self.displayedCount_focusregion = 0
        self.nr = 0
        self.focusregionList.clear()

        sorted_results = self.sortResults_focusregion(filtered)
        first_batch = sorted_results[:50]
        self.setResultsToList(first_batch, self.focusregionList)
        self.displayedCount_focusregion = len(first_batch)

        total = len(filtered)
        if total == 0:
            if nur_neuigkeiten:
                lv_display = ""
                if last_visit:
                    try:
                        lv_display = datetime.strptime(
                            last_visit[:10], '%Y-%m-%d').strftime('%d.%m.%Y')
                    except Exception:
                        pass
                fr_name = fr.name or fr.id
                lv_part = f" am <b>{lv_display}</b>" if lv_display else ""

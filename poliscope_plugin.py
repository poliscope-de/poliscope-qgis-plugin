class _ToolTipBlocker(QObject):
    """Consumes QEvent.ToolTip so child widgets don't inherit parent tooltips."""
    def eventFilter(self, obj, event):
        if event.type() == QEvent.ToolTip:
            return True
        return super().eventFilter(obj, event)


